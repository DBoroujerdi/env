-module(env_app).

-behaviour(application).

-export([start/2,
         stop/1]).

%%------------------------------------------------------------------------------
%% public
%%------------------------------------------------------------------------------

start(_Type, _Args) ->
    _ = init_cache(),

    env_sup:start_link().

stop(_State) ->
    ok.


%%------------------------------------------------------------------------------
%% private
%%------------------------------------------------------------------------------

%% cache

init_cache() ->
    ets:new(env, [public, set, named_table]).
